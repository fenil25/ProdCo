    private void addDepartmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDepartmentActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat ft = new SimpleDateFormat("yyyy-MM-dd");
        String name = nameField.getText();
        String location = locationField.getText();
        String start_date = ft.format(startDateField.getDate());
        String man_id = emp.get(managerField.getSelectedItem().toString())+"";
        
        if(name.equals("") || location.equals("") || start_date.equals("")) {
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, "Some required fields are missing.");
        }
        
        String query1, query2;
        if(man_id.equals("None")){
            query1 = String.format("INSERT INTO DEPARTMENT (name, location, start_date) VALUES('%s', '%s', '%s');", 
                    name, location, start_date);
            query2 = String.format("UPDATE DEPARTMENT SET name = '%s', location = '%s', start_date = '%s'"
                    + " WHERE idDepartment = %s;",
                    name, location, start_date, deptId);
        }else{    
            query1 = String.format("INSERT INTO DEPARTMENT (name, location, start_date, manager_id) VALUES('%s', '%s', '%s', %s);", 
                    name, location, start_date, man_id);
            query2 = String.format("UPDATE DEPARTMENT SET name = '%s', location = '%s', start_date = '%s', manager_id = %s"
                    + " WHERE idDepartment = %s;",
                    name, location, start_date, man_id, deptId);
        }
        
        System.out.println(query1);
        System.out.println(query2);
        
        try {
            Statement addq = con.createStatement();
            JFrame f = new JFrame();
            if(updateFlag == 0){
                addq.executeUpdate(query1);
                JOptionPane.showMessageDialog(f, "Department added successfully");
            } else {
                addq.executeUpdate(query2);
                JOptionPane.showMessageDialog(f, "Department updated successfully");
            }
            DisplayDepartment dise;
            try {
                dise = new DisplayDepartment(con);
                dise.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(f, "Something Went Wrong");
            }
        } catch (SQLIntegrityConstraintViolationException e){
                JFrame f = new JFrame();
                JOptionPane.showMessageDialog(f, "An Employee cannot Manage Two Departments Simultaneously.");
        } catch (SQLException ex) {
            Logger.getLogger(AddDepartment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addDepartmentActionPerformed

