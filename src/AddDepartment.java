    public AddDepartment(Connection con, int updateFlag, int did, String name, String loc, String startdate, String manager) throws SQLException, ParseException {
        this.con = con;
        this.updateFlag = updateFlag;
        this.deptId = did;
        initComponents();
        Statement st = con.createStatement();
        ResultSet r = st.executeQuery("SELECT * FROM Employee");
        managerField.addItem("None");
        while(r.next()){
            managerField.addItem(r.getString("idEmployee") + ": " + r.getString("first_name") + " " + r.getString("last_name"));
            emp.put(r.getString("idEmployee") + ": " + r.getString("first_name") + " " + r.getString("last_name"), Integer.parseInt(r.getString("idEmployee")));
        }
        AutoCompletion.enable(managerField);
        if(updateFlag == 1){
            nameField.setText(name);
            locationField.setText(loc);
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            startDateField.setDate(df.parse(startdate));
            managerField.setSelectedItem(manager);
            addDepartment.setText("Update Department");
        }
    }
    

