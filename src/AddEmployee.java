    AddEmployee(Connection con, int updateFlag, int ed, String fn, String ln, String db, String ad, String ph, String de, String dj, 
            String po, String sal, String tb) throws SQLException, ParseException {
        this.con = con;
        this.updateFlag = updateFlag;
        this.employeeID = ed;
        initComponents();
        Statement st = con.createStatement();
        ResultSet r = st.executeQuery("SELECT * FROM DEPARTMENT");
        while(r.next()){
            departmentvalue.addItem(r.getString("name"));
            dept.put(r.getString("name"), Integer.parseInt(r.getString("idDepartment")));
        }
        AutoCompletion.enable(departmentvalue);
        if(updateFlag == 1){
            fnamefield.setText(fn);
            lnamefield.setText(ln);
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            dobfield.setDate(df.parse(db));
            addressfield.setText(ad);
            phonefield.setText(ph);
            departmentvalue.setSelectedItem(de);
            dojfield.setDate(df.parse(dj));
            positionfield.setText(po);
            salaryfield.setText(sal);
            taxbracketfield.setText(tb);
            addEmployee.setText("Update Employee");
        }
    }

